    for j = 1:n
        % 局部移情模型公式:
        % u_j(a_i) = w_jj * uI_j(a_i) + Σ_{k≠j} w_jk * uI_k(a_i) - Σ_{k≠j} c_jk(a_i)
        
        % 内在效用部分
