                empathetic_part = empathetic_part + W(j,k) * uI(k, a_idx);
            end
        end
        
        % 维护成本部分：∑_{k≠j} c_jk(a_i)
        cost_part = 0;
        for k = 1:n
            if k ~= j
                cost_part = cost_part + C{a_idx}(j,k);
            end
        end
        
        % 更新当前效用（仅一次迭代）
        u_final(j, a_idx) = intrinsic_part + empathetic_part - cost_part;
        sw_current = sw_current + u_final(j, a_idx);
        
